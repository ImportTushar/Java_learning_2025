public class Print2largest {
    public static void main(String[] args) {
        int A = 1;
        int B = 7;

        if(A > B){
            System.out.println("The Largest Number is A  " + A);
        }

        else if (A == B) {
            System.out.println("Both A  and  B are Largest");
        }

        else{
            System.out.println("The Largest number is B  " + B);
        }
    }
}
