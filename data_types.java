public class data_types {
    public static void main(String[] args) {
        int rollno = 64;
        char letter = 'r';
        float marks = 98.67f;
        double largeDecimalNumbers = 456789.123456;
        long largeInteger = 456789123;
        boolean check = true;
        byte smallNumber = 120;
        short mediumNumber = 32000;

        System.out.println("Roll Number: " + rollno);
        System.out.println("Letter: " + letter);
        System.out.println("Marks: " + marks);
        System.out.println("Large Decimal Numbers: " + largeDecimalNumbers);
        System.out.println("Large Integer: " + largeInteger);
        System.out.println("Check: " + check);
        System.out.println("Small Number: " + smallNumber);
        System.out.println("Medium Number: " + mediumNumber);
    }
}
